public class NavBarException extends RuntimeException {

    public NavBarException() {
        super();
    }

    public NavBarException(String message) {
        super(message);
    }

    public NavBarException(Throwable cause) {
        super(cause);
    }

    public NavBarException(String message, Throwable cause) {
        super(message, cause);
    }

}
